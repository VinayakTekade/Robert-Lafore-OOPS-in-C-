//intvars.cpp
//demonstrates integer variables
#include <iostream>

using namespace std;

int main()
{
   int var1,var2;                               //define var1 and var2
   var1=20;                                     //assign value to var1
   var2=var1+10;                                //assign value to var2
   cout<<"var1+10 is "<<var2<<endl;             //output text
   return 0;
}

